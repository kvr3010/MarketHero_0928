package com.markethero.vo;

public class MerchantVO {

	private int id;
	private String email;
	private String pw;
	private String name;
	private String tel;
	private String bank;
	private String ac_name;
	private String ac_num;
	private String co_reg_num;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	public String getAc_name() {
		return ac_name;
	}
	public void setAc_name(String ac_name) {
		this.ac_name = ac_name;
	}
	public String getAc_num() {
		return ac_num;
	}
	public void setAc_num(String ac_num) {
		this.ac_num = ac_num;
	}
	public String getCo_reg_num() {
		return co_reg_num;
	}
	public void setCo_reg_num(String co_reg_num) {
		this.co_reg_num = co_reg_num;
	}

	

	
}
